    /**
     * Method that saves all input data of the dialog
     *
     * @return
     * @throws NullPointerException
     */
    public Map<String, Object> getInitParams() throws NullPointerException {
        if (speicherort == null) {
            speicherort = new File(System.getProperty("user.home") + File.separator + "Documents" + File.separator + "rec.xml");
        }
        Map<String, Object> initParams = Stream.of(new Object[][]{
            {lbMarker.getText().trim(), markerName},
            {lbSimulationFile.getText().trim(), simulationFiles},
            {lbSquash.getText().trim(), squashText}
        //,{lbSpeicherort.getText().trim(), speicherort}
        }).collect(Collectors.toMap(data -> (String) data[0], data -> data[1]));
        return initParams;
    }

    /**
     * Method that add a new ExtensionFilter to the JFileChooser for simulation files
     *
     * @param extension
     * @param desciption
     */
    private void addNewExtensionFilter(String extension, String description) {
        FileNameExtensionFilter filter = null;
        int cnt = 0;
        List<String> extensions = new ArrayList<>();
        if (extension.contains(";")) {
            for (String string : extension.split(";")) {
                if (cnt != 0) {
                    String[] ext = filter.getExtensions();
                    for (String string1 : ext) {
                        extensions.add(string1);
                    }
                    extensions.add(string);
                    String[] simpleArray = new String[extensions.size()];
                    extensions.toArray(simpleArray);
                    filter = new FileNameExtensionFilter(filter.getDescription(), simpleArray);
                } else {
                    filter = new FileNameExtensionFilter(description.trim(), string.trim());
                }
                cnt++;
            }
        } else {
            filter = new FileNameExtensionFilter(description.trim(), extension.trim());
        }
        fchSimulation.setFileFilter(filter);
    }

