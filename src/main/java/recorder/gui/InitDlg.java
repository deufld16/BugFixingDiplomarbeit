    public InitDlg(java.awt.Frame parent, boolean modal) {
        super(parent, modal);
        this.parent = parent;
        fchSimulation = new JFileChooser();
        try {
            fileExtensions = IOLoader.getExtensions();
            extensionDescriptions = IOLoader.getExtensionsDescription();
            FileNameExtensionFilter filter = null;
            for (int i = 0; i < fileExtensions.size(); i++) {
                int cnt = 0;
                List<String> extensions = new ArrayList<>();
                if (fileExtensions.get(i).contains(";")) {
                    for (String string : fileExtensions.get(i).split(";")) {
                        if (cnt != 0) {
                            extensions.add(filter.getExtensions()[0]);
                            extensions.add(string);
                            String[] simpleArray = new String[extensions.size()];
                            extensions.toArray(simpleArray);
                            filter = new FileNameExtensionFilter(filter.getDescription(), simpleArray);
                        } else {
                            filter = new FileNameExtensionFilter(extensionDescriptions.get(i).trim(), string.trim());
                        }
                        cnt++;
                    }
                } else {
                    filter = new FileNameExtensionFilter(extensionDescriptions.get(i).trim(), fileExtensions.get(i).trim());
                }
                fchSimulation.setFileFilter(filter);
            }
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Die Konfigurationsdatei hat einen Fehler - bitte geben Sie die Grenzen manuell ein", "Fehler", JOptionPane.ERROR_MESSAGE);
        }
        initComponents();
        this.setLocationRelativeTo(null);
        setSize(new Dimension(850, 500));
//        try {
//            UIManager.setLookAndFeel("com.sun.java.swing.plaf.nimbus.NimbusLookAndFeel");
//        } catch (Exception ex) {
//            ex.printStackTrace();
//        }
        paMain.setBorder(new EmptyBorder(35, 0, 0, 0));
        if (GUIOperations.getTextForInitDialog() != null) {
            tfMarker.setText(GUIOperations.getTextForInitDialog());
            tfMarker.setEditable(false);
        }
    }

