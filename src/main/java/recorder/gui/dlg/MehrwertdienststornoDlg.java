    /**
     * Method that is called when the user presses OK
     *
     * @param evt
     */
    private void onOk(ActionEvent evt) {
        this.ok = true;
        if (pfeiltaste) {
            String part_1 = ((JRadioButton) (((JPanel) (paDynamicContent.getComponent(0))).getComponent(0))).isSelected() ? "up" : "down";
            String part_2 = ((JRadioButton) (((JPanel) (paDynamicContent.getComponent(1))).getComponent(0))).isSelected() ? "up" : "down";
            displayText = String.format("%s,%s", part_1, part_2);
        } else {
            try {
                int tmp_int = Integer.parseInt(((JTextField) (paDynamicContent.getComponent(0))).getText());
                displayText = tmp_int + "";
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(GUIOperations.getMainframe(), "Bitte geben Sie eine valide Zahl ein!");
                return;
            }
        }
        dispose();
    }

